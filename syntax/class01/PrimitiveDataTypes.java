package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		/* * Primitive Data types
		 * 
		 * byte
		 * short
		 * int
		 * long
		 * float
		 * double
		 * char
		 * boolean
		 */
		// Whole Number Data Type
	byte num = 10;	
	
	short num1 = 100;
	
	int num2 = 1000;
	
	// l or L
	long numb3 = 1000000000000l;
	
	
	// Decimal Number Data Types
	
	// f or F
	float numb4 = 12.9f;
	
	double num5 = 0.888;
	
	// Non-Number Data Types
	
	char add = '+';
	char letter = 'a';
	
	// true or false (Yes or No)
	// do not put Yes or No for value
	boolean hungry = true;
	boolean thirsty = false;

	}

}
