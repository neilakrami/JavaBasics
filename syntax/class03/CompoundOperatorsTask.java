package com.syntax.class03;

public class CompoundOperatorsTask {

	public static void main(String[] args) {
		
		
		int n1 = 50;
		n1 += 100;
		System.out.println(n1);
		
		n1 -= 67;
		System.out.println(n1);
		
		int cakePiece = 11;
		cakePiece /= 4;
		System.out.println(cakePiece);
		
		cakePiece = 25;
		cakePiece %= 7;
		System.out.println(cakePiece);
	}

}
